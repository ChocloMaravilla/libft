#include "libft.h"

void ft_putnbr_fd(int fd, int n)
{
    if(n == -2147483648)
    {
      ft_putchar_fd("-2147483648", fd);  
    }
    if(n > -2147483648)
    {
        n *= -1;
    }
    if (!(n/10 < 0) || !(n/10 == 0))
    {
        ft_putnbr_fd(n/10, fd);
    }
    else
    {
        ft_putchar_fd('-', fd);
        n = n * -1;
    }
}
